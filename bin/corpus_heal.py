#!/usr/bin/env python3
"""corpus_heal.py — safe, deterministic self-healing for the corpus (no LLM).

The daily ingest accumulates broken source citations: a page cites `](../../raw/_inbox/foo.md)`
but the raw file MOVED to `raw/web/foo.md` when it was ingested (or the page's relative depth is
wrong for its type). Both are fixable without judgment: find the raw file by basename and
recompute the correct relative path from the citing page. Only a UNIQUE basename match is
repointed — ambiguous or truly-missing citations are left for a human (reported, never guessed).

Reuses corpus_lint's page scan + citation regex (single source of truth). Dry-run by default;
--apply writes. Wired into the nightly after ingest, before the integrity lint.
"""
from __future__ import annotations

import argparse
import json
import os
import sys
from pathlib import Path

BIN = Path(__file__).resolve().parent
ROOT = BIN.parent
sys.path.insert(0, str(BIN))
import corpus_lint as cl  # noqa: E402 — reuse content_pages + _CITATION_RE

RAW = ROOT / "raw"


def build_raw_index(raw_root: Path) -> dict:
    """basename -> [absolute paths] for every raw .md file."""
    idx: dict = {}
    for f in raw_root.rglob("*.md"):
        idx.setdefault(f.name, []).append(f.resolve())
    return idx


def repair_citations(corpus: Path, raw_root: Path, *, apply: bool = False) -> dict:
    """Repoint broken `](../..raw/..md)` citations to the unique moved raw file.

    Returns {repaired, ambiguous, missing, pages_changed, missing_list}.
    """
    idx = build_raw_index(raw_root)
    t = {"repaired": 0, "ambiguous": 0, "missing": 0, "pages_changed": 0, "missing_list": []}
    for p in cl.content_pages(corpus):
        text = p.read_text(encoding="utf-8", errors="ignore")
        new = text
        for rel in cl._CITATION_RE.findall(text):
            if (p.parent / rel).resolve().exists():
                continue                                   # citation is fine
            hits = idx.get(Path(rel).name, [])
            if len(hits) == 1:
                newrel = os.path.relpath(hits[0], p.parent.resolve())
                if newrel != rel:
                    new = new.replace(f"]({rel})", f"]({newrel})")
                    t["repaired"] += 1
            elif len(hits) > 1:
                t["ambiguous"] += 1
            else:
                t["missing"] += 1
                t["missing_list"].append(f"{p.relative_to(corpus.parent)} -> {rel}")
        if new != text:
            t["pages_changed"] += 1
            if apply:
                p.write_text(new, encoding="utf-8")
    return t


import re

_H1_RE = re.compile(r"^#\s+(.+?)\s*$", re.M)
_TYPE_RE = re.compile(r"^type:\s*(\w+)", re.M)
AUTO_START = "<!-- AUTO-INDEX:START (generated by bin/corpus_heal.py hubs — do not edit inside) -->"
AUTO_END = "<!-- AUTO-INDEX:END -->"
_GROUPS = [("concept", "Concepts"), ("entity", "Entities"), ("synthesis", "Syntheses")]


def _page_meta(path: Path) -> tuple[str, str]:
    """(type, title) for a page — title from its first '# ' heading, else the slug prettified."""
    text = path.read_text(encoding="utf-8", errors="ignore")
    tm = _TYPE_RE.search(text)
    typ = tm.group(1) if tm else "?"
    body = text.split("---", 2)[-1] if text.startswith("---") else text
    hm = _H1_RE.search(body)
    title = hm.group(1).strip() if hm else path.stem.replace("-", " ")
    return typ, title


def build_hub_index(domain_dir: Path, corpus: Path) -> str:
    """Delimited auto-index section for a domain README: knowledge pages listed openly,
    source summaries collapsed. Root-relative links, alphabetical (stable ordering)."""
    groups: dict = {}
    for p in domain_dir.rglob("*.md"):
        if p.name == "README.md":
            continue
        typ, title = _page_meta(p)
        rel = "/" + str(p.relative_to(corpus)).replace("\\", "/")
        groups.setdefault(typ, []).append((title, rel))
    lines = [AUTO_START, "", "## Pages in this domain", ""]
    for key, label in _GROUPS:
        items = sorted(groups.get(key, []), key=lambda x: x[0].lower())
        if items:
            lines.append(f"### {label} ({len(items)})")
            lines += [f"- [{title}]({rel})" for title, rel in items]
            lines.append("")
    sources = sorted(groups.get("source", []), key=lambda x: x[0].lower())
    if sources:
        lines += ["<details>", f"<summary>Source summaries ({len(sources)})</summary>", ""]
        lines += [f"- [{title}]({rel})" for title, rel in sources]
        lines += ["", "</details>", ""]
    lines.append(AUTO_END)
    return "\n".join(lines)


def update_hub(readme: Path, section: str, *, apply: bool = False) -> bool:
    """Replace the AUTO-INDEX block (or append it) in a README, preserving all other content."""
    text = readme.read_text(encoding="utf-8", errors="ignore") if readme.exists() else \
        f"# {readme.parent.name}\n"
    if AUTO_START in text and AUTO_END in text:
        pre = text.split(AUTO_START)[0].rstrip()
        post = text.split(AUTO_END, 1)[1].lstrip()
        new = pre + "\n\n" + section + ("\n\n" + post if post else "\n")
    else:
        new = text.rstrip() + "\n\n" + section + "\n"
    if new != text and apply:
        readme.write_text(new, encoding="utf-8")
    return new != text


def index_hubs(corpus: Path, *, apply: bool = False) -> dict:
    """Regenerate the auto-index in every domain README. Returns counts."""
    t = {"domains_changed": 0, "pages_indexed": 0}
    for d in cl._domain_dirs(corpus):
        section = build_hub_index(d, corpus)
        t["pages_indexed"] += section.count("\n- [")
        if update_hub(d / "README.md", section, apply=apply):
            t["domains_changed"] += 1
    return t


def cmd_hubs(args) -> int:
    corpus = Path(args.corpus) if args.corpus else ROOT / "corpus"
    t = index_hubs(corpus, apply=args.apply)
    print(json.dumps({**t, "applied": bool(args.apply)}, indent=2))
    return 0


RELATED_START = "<!-- RELATED:START (generated by bin/corpus_heal.py related — do not edit inside) -->"
RELATED_END = "<!-- RELATED:END -->"
_ALIASES_RE = re.compile(r"^aliases:\s*$((?:\n\s+-\s+.+)+)", re.M)
_TAGS_RE = re.compile(r"^tags:\s*$((?:\n\s+-\s+.+)+)", re.M)
_LIST_ITEM_RE = re.compile(r"^\s+-\s+(.+?)\s*$", re.M)
_GENERIC_TAG_RE = re.compile(r"^(corpus/|source$|hub$|entity$|concept$|synthesis$|.*-quick-intake$)")
# common tokens that co-occur across unrelated technical pages — excluded from the token signal
_STOP = {"machine", "learning", "data", "system", "systems", "engineering", "science",
         "model", "models", "code", "guide", "introduction", "using", "with", "from",
         "2025", "2026", "development", "based", "your", "that", "this", "what"}
_KNOWLEDGE = ("concept", "entity", "synthesis")


def page_signals(text: str, slug: str) -> tuple[set, set, str]:
    """(signals, aliases, title) for a knowledge page. `signals` = non-generic tags + aliases +
    title/slug tokens (≥4 chars, not stopwords) — the deterministic relatedness signal."""
    sig: set = set()
    aliases: set = set()
    tb = _TAGS_RE.search(text)
    if tb:
        for t in _LIST_ITEM_RE.findall(tb.group(1)):
            t = t.strip()
            if not _GENERIC_TAG_RE.match(t):
                sig.add(t.lower())
    ab = _ALIASES_RE.search(text)
    if ab:
        for a in _LIST_ITEM_RE.findall(ab.group(1)):
            a = a.strip().lower()
            sig.add(a)
            aliases.add(a)
    body = text.split("---", 2)[-1] if text.startswith("---") else text
    hm = _H1_RE.search(body)
    title = hm.group(1).strip() if hm else slug.replace("-", " ")
    for tok in re.split(r"[^a-z0-9]+", (title + " " + slug).lower()):
        if len(tok) >= 4 and tok not in _STOP:
            sig.add(tok)
    return sig, aliases, title


def _links_in(text: str) -> set:
    """Root-relative link targets `/domain/slug` already present in a page (prose or sections)."""
    return {m for m in re.findall(r"\]\(/([a-z0-9-]+/[a-z0-9-]+)\.md\)", text)}


def build_related(corpus: Path, *, min_shared: int = 2) -> tuple[dict, dict]:
    """Symmetric cross-domain relations between knowledge pages. Returns
    (relations: node_id -> [(other_node, other_title, other_domain, shared)], nodes: node_id -> Path).
    A pair relates if it shares ≥min_shared signals OR a common alias, is cross-domain, and is not
    already linked either way (outside the RELATED block)."""
    pages = []
    nodes: dict = {}
    for d in cl._domain_dirs(corpus):
        for p in d.rglob("*.md"):
            if p.name == "README.md":
                continue
            text = p.read_text(encoding="utf-8", errors="ignore")
            tm = _TYPE_RE.search(text)
            if not tm or tm.group(1) not in _KNOWLEDGE:
                continue
            sig, aliases, title = page_signals(text, p.stem)
            node = f"{d.name}/{p.stem}"
            # existing links OUTSIDE the RELATED block (so a prior auto-link doesn't suppress refresh)
            outside = text
            if RELATED_START in text:
                outside = text.split(RELATED_START)[0] + text.split(RELATED_END, 1)[-1]
            pages.append({"node": node, "domain": d.name, "title": title,
                          "sig": sig, "aliases": aliases, "links": _links_in(outside)})
            nodes[node] = p
    from itertools import combinations
    from collections import defaultdict
    rel = defaultdict(list)
    for a, b in combinations(pages, 2):
        if a["domain"] == b["domain"]:
            continue
        shared = a["sig"] & b["sig"]
        if not (len(shared) >= min_shared or (a["aliases"] & b["aliases"])):
            continue
        if b["node"] in a["links"] or a["node"] in b["links"]:
            continue
        rel[a["node"]].append((b["node"], b["title"], b["domain"], len(shared)))
        rel[b["node"]].append((a["node"], a["title"], a["domain"], len(shared)))
    return dict(rel), nodes


def render_related(links: list) -> str:
    lines = [RELATED_START, "", "## Related across domains", ""]
    for node, title, dom, shared in sorted(links, key=lambda x: (-x[3], x[1].lower())):
        lines.append(f"- [{title}](/{node}.md) · _{dom}_")
    lines += ["", RELATED_END]
    return "\n".join(lines)


def update_related(page: Path, section: str | None, *, apply: bool = False) -> bool:
    """Replace/insert the RELATED block, or remove it when section is None. Preserves all other
    content. Returns True if the file would change."""
    text = page.read_text(encoding="utf-8", errors="ignore")
    has = RELATED_START in text and RELATED_END in text
    if section is None:
        if not has:
            return False
        pre = text.split(RELATED_START)[0].rstrip()
        post = text.split(RELATED_END, 1)[1].lstrip()
        new = pre + ("\n\n" + post if post else "\n")
    elif has:
        pre = text.split(RELATED_START)[0].rstrip()
        post = text.split(RELATED_END, 1)[1].lstrip()
        new = pre + "\n\n" + section + ("\n\n" + post if post else "\n")
    else:
        new = text.rstrip() + "\n\n" + section + "\n"
    if new != text and apply:
        page.write_text(new, encoding="utf-8")
    return new != text


def index_related(corpus: Path, *, apply: bool = False, min_shared: int = 2) -> dict:
    """Regenerate the '## Related across domains' block on every knowledge page (add where related,
    remove stale blocks where not). Deterministic + reversible."""
    rel, nodes = build_related(corpus, min_shared=min_shared)
    t = {"pages_linked": 0, "links": 0, "blocks_removed": 0}
    for node, p in nodes.items():
        links = rel.get(node)
        if links:
            if update_related(p, render_related(links), apply=apply):
                t["pages_linked"] += 1
            t["links"] += len(links)
        else:
            if update_related(p, None, apply=apply):
                t["blocks_removed"] += 1
    t["pairs"] = sum(len(v) for v in rel.values()) // 2
    return t


def cmd_related(args) -> int:
    corpus = Path(args.corpus) if args.corpus else ROOT / "corpus"
    t = index_related(corpus, apply=args.apply, min_shared=args.min_shared)
    print(json.dumps({**t, "applied": bool(args.apply)}, indent=2))
    return 0


def cmd_citations(args) -> int:
    corpus = Path(args.corpus) if args.corpus else ROOT / "corpus"
    raw_root = Path(args.raw) if args.raw else RAW
    t = repair_citations(corpus, raw_root, apply=args.apply)
    print(json.dumps({**t, "missing_list": t["missing_list"][:20],
                      "applied": bool(args.apply)}, indent=2))
    return 0


def _args(argv):
    p = argparse.ArgumentParser(description="Deterministic corpus self-healing.")
    sub = p.add_subparsers(dest="cmd", required=True)
    c = sub.add_parser("citations", help="Repoint broken source citations to moved raw files.")
    c.add_argument("--corpus", default=None)
    c.add_argument("--raw", default=None)
    c.add_argument("--apply", action="store_true", help="write changes (default: dry-run)")
    c.set_defaults(func=cmd_citations)

    r = sub.add_parser("related", help="Add a cross-domain 'Related across domains' block to knowledge pages.")
    r.add_argument("--corpus", default=None)
    r.add_argument("--min-shared", type=int, default=2)
    r.add_argument("--apply", action="store_true", help="write changes (default: dry-run)")
    r.set_defaults(func=cmd_related)

    h = sub.add_parser("hubs", help="Regenerate the auto-index in each domain README (zeroes orphans).")
    h.add_argument("--corpus", default=None)
    h.add_argument("--apply", action="store_true", help="write changes (default: dry-run)")
    h.set_defaults(func=cmd_hubs)
    return p.parse_args(argv)


def main(argv=None) -> int:
    args = _args(argv)
    return args.func(args)


if __name__ == "__main__":
    raise SystemExit(main())
