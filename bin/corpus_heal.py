#!/usr/bin/env python3
"""corpus_heal.py — safe, deterministic self-healing for the corpus (no LLM).

The daily ingest accumulates broken source citations: a page cites `](../../raw/_inbox/foo.md)`
but the raw file MOVED to `raw/web/foo.md` when it was ingested (or the page's relative depth is
wrong for its type). Both are fixable without judgment: find the raw file by basename and
recompute the correct relative path from the citing page. Only a UNIQUE basename match is
repointed — ambiguous or truly-missing citations are left for a human (reported, never guessed).

Reuses corpus_lint's page scan + citation regex (single source of truth). Dry-run by default;
--apply writes. Wired into the nightly after ingest, before the integrity lint.
"""
from __future__ import annotations

import argparse
import json
import os
import sys
from pathlib import Path

BIN = Path(__file__).resolve().parent
ROOT = BIN.parent
sys.path.insert(0, str(BIN))
import corpus_lint as cl  # noqa: E402 — reuse content_pages + _CITATION_RE

RAW = ROOT / "raw"


def build_raw_index(raw_root: Path) -> dict:
    """basename -> [absolute paths] for every raw .md file."""
    idx: dict = {}
    for f in raw_root.rglob("*.md"):
        idx.setdefault(f.name, []).append(f.resolve())
    return idx


def repair_citations(corpus: Path, raw_root: Path, *, apply: bool = False) -> dict:
    """Repoint broken `](../..raw/..md)` citations to the unique moved raw file.

    Returns {repaired, ambiguous, missing, pages_changed, missing_list}.
    """
    idx = build_raw_index(raw_root)
    t = {"repaired": 0, "ambiguous": 0, "missing": 0, "pages_changed": 0, "missing_list": []}
    for p in cl.content_pages(corpus):
        text = p.read_text(encoding="utf-8", errors="ignore")
        new = text
        for rel in cl._CITATION_RE.findall(text):
            if (p.parent / rel).resolve().exists():
                continue                                   # citation is fine
            hits = idx.get(Path(rel).name, [])
            if len(hits) == 1:
                newrel = os.path.relpath(hits[0], p.parent.resolve())
                if newrel != rel:
                    new = new.replace(f"]({rel})", f"]({newrel})")
                    t["repaired"] += 1
            elif len(hits) > 1:
                t["ambiguous"] += 1
            else:
                t["missing"] += 1
                t["missing_list"].append(f"{p.relative_to(corpus.parent)} -> {rel}")
        if new != text:
            t["pages_changed"] += 1
            if apply:
                p.write_text(new, encoding="utf-8")
    return t


import re

_H1_RE = re.compile(r"^#\s+(.+?)\s*$", re.M)
_TYPE_RE = re.compile(r"^type:\s*(\w+)", re.M)
AUTO_START = "<!-- AUTO-INDEX:START (generated by bin/corpus_heal.py hubs — do not edit inside) -->"
AUTO_END = "<!-- AUTO-INDEX:END -->"
_GROUPS = [("concept", "Concepts"), ("entity", "Entities"), ("synthesis", "Syntheses")]


def _page_meta(path: Path) -> tuple[str, str]:
    """(type, title) for a page — title from its first '# ' heading, else the slug prettified."""
    text = path.read_text(encoding="utf-8", errors="ignore")
    tm = _TYPE_RE.search(text)
    typ = tm.group(1) if tm else "?"
    body = text.split("---", 2)[-1] if text.startswith("---") else text
    hm = _H1_RE.search(body)
    title = hm.group(1).strip() if hm else path.stem.replace("-", " ")
    return typ, title


def build_hub_index(domain_dir: Path, corpus: Path) -> str:
    """Delimited auto-index section for a domain README: knowledge pages listed openly,
    source summaries collapsed. Root-relative links, alphabetical (stable ordering)."""
    groups: dict = {}
    for p in domain_dir.rglob("*.md"):
        if p.name == "README.md":
            continue
        typ, title = _page_meta(p)
        rel = "/" + str(p.relative_to(corpus)).replace("\\", "/")
        groups.setdefault(typ, []).append((title, rel))
    lines = [AUTO_START, "", "## Pages in this domain", ""]
    for key, label in _GROUPS:
        items = sorted(groups.get(key, []), key=lambda x: x[0].lower())
        if items:
            lines.append(f"### {label} ({len(items)})")
            lines += [f"- [{title}]({rel})" for title, rel in items]
            lines.append("")
    sources = sorted(groups.get("source", []), key=lambda x: x[0].lower())
    if sources:
        lines += ["<details>", f"<summary>Source summaries ({len(sources)})</summary>", ""]
        lines += [f"- [{title}]({rel})" for title, rel in sources]
        lines += ["", "</details>", ""]
    lines.append(AUTO_END)
    return "\n".join(lines)


def update_hub(readme: Path, section: str, *, apply: bool = False) -> bool:
    """Replace the AUTO-INDEX block (or append it) in a README, preserving all other content."""
    text = readme.read_text(encoding="utf-8", errors="ignore") if readme.exists() else \
        f"# {readme.parent.name}\n"
    if AUTO_START in text and AUTO_END in text:
        pre = text.split(AUTO_START)[0].rstrip()
        post = text.split(AUTO_END, 1)[1].lstrip()
        new = pre + "\n\n" + section + ("\n\n" + post if post else "\n")
    else:
        new = text.rstrip() + "\n\n" + section + "\n"
    if new != text and apply:
        readme.write_text(new, encoding="utf-8")
    return new != text


def index_hubs(corpus: Path, *, apply: bool = False) -> dict:
    """Regenerate the auto-index in every domain README. Returns counts."""
    t = {"domains_changed": 0, "pages_indexed": 0}
    for d in cl._domain_dirs(corpus):
        section = build_hub_index(d, corpus)
        t["pages_indexed"] += section.count("\n- [")
        if update_hub(d / "README.md", section, apply=apply):
            t["domains_changed"] += 1
    return t


def cmd_hubs(args) -> int:
    corpus = Path(args.corpus) if args.corpus else ROOT / "corpus"
    t = index_hubs(corpus, apply=args.apply)
    print(json.dumps({**t, "applied": bool(args.apply)}, indent=2))
    return 0


def cmd_citations(args) -> int:
    corpus = Path(args.corpus) if args.corpus else ROOT / "corpus"
    raw_root = Path(args.raw) if args.raw else RAW
    t = repair_citations(corpus, raw_root, apply=args.apply)
    print(json.dumps({**t, "missing_list": t["missing_list"][:20],
                      "applied": bool(args.apply)}, indent=2))
    return 0


def _args(argv):
    p = argparse.ArgumentParser(description="Deterministic corpus self-healing.")
    sub = p.add_subparsers(dest="cmd", required=True)
    c = sub.add_parser("citations", help="Repoint broken source citations to moved raw files.")
    c.add_argument("--corpus", default=None)
    c.add_argument("--raw", default=None)
    c.add_argument("--apply", action="store_true", help="write changes (default: dry-run)")
    c.set_defaults(func=cmd_citations)

    h = sub.add_parser("hubs", help="Regenerate the auto-index in each domain README (zeroes orphans).")
    h.add_argument("--corpus", default=None)
    h.add_argument("--apply", action="store_true", help="write changes (default: dry-run)")
    h.set_defaults(func=cmd_hubs)
    return p.parse_args(argv)


def main(argv=None) -> int:
    args = _args(argv)
    return args.func(args)


if __name__ == "__main__":
    raise SystemExit(main())
